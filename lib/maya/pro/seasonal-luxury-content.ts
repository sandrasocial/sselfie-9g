/**
 * Detect seasonal context
 */
export function detectSeasonalContent(text: string): {
  season: 'christmas' | 'new-years' | null
  style: 'cozy' | 'elegant' | 'party' | null
  outfit: 'loungewear' | 'casual' | 'formal' | null
} {
  const textLower = text.toLowerCase()

  // Detect season
  let season: 'christmas' | 'new-years' | null = null
  if (/christmas|holiday|festive|xmas|tree|santa|winter.*holiday/i.test(textLower)) {
    season = 'christmas'
  } else if (/new.*year|nye|midnight|countdown|champagne.*toast|new.*year.*eve/i.test(textLower)) {
    season = 'new-years'
  }

  // Detect style
  let style: 'cozy' | 'elegant' | 'party' | null = null
  if (/cozy|comfort|lounge|relax|home/i.test(textLower)) {
    style = 'cozy'
  } else if (/elegant|sophisticated|luxury|formal|glamour/i.test(textLower)) {
    style = 'elegant'
  } else if (/party|celebration|toast|dance|celebrate/i.test(textLower)) {
    style = 'party'
  }

  // Detect outfit type
  let outfit: 'loungewear' | 'casual' | 'formal' | null = null
  if (/pajama|lounge|robe|cozy.*wear|comfort/i.test(textLower)) {
    outfit = 'loungewear'
  } else if (/dress.*formal|gown|elegant.*dress|cocktail/i.test(textLower)) {
    outfit = 'formal'
  } else if (/casual|sweater.*dress|knit.*dress/i.test(textLower)) {
    outfit = 'casual'
  }

  return { season, style, outfit }
}
