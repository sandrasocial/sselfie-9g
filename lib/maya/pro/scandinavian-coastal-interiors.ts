/**
 * Helper function to build complete Scandinavian coastal interior setting
 */
export function buildScandinavianCoastalInterior(
  room: 'living' | 'bedroom' | 'kitchen' | 'bathroom' | 'dining',
  timeOfDay: 'morning' | 'afternoon' | 'evening' = 'morning',
  style: 'coastal_luxury' | 'luxury_urban' | 'serene' | 'modern' | 'elegant' = 'coastal_luxury'
): string {
  const furniture = SCANDINAVIAN_FURNITURE
  const flooring = SCANDINAVIAN_FLOORING
  const textiles = SCANDINAVIAN_TEXTILES
  const decor = SCANDINAVIAN_DECOR
  const architecture = SCANDINAVIAN_ARCHITECTURE
  const palette = SCANDINAVIAN_COLOR_PALETTE
  const lighting = SCANDINAVIAN_LIGHTING
  const mood = SCANDINAVIAN_MOOD

  // Random selection helper
  const pick = <T>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)]

  // Build setting based on room type
  let setting = ''

  switch (room) {
    case 'living':
      setting = `Scandinavian coastal living room with ${pick(furniture.SOFAS)}, ${pick(flooring.WOOD_FLOORS)}, ${pick(architecture.WINDOWS)}, ${pick(furniture.COFFEE_TABLES)}, ${pick(decor.CERAMICS)} on side table, ${pick(textiles.THROWS)} draped casually, ${pick(palette.OVERALL)}, ${timeOfDay === 'morning' ? pick(lighting.MORNING) : timeOfDay === 'afternoon' ? pick(lighting.AFTERNOON) : pick(lighting.EVENING)}, ${pick(mood.ASPIRATIONAL)}`
      break

    case 'bedroom':
      setting = `Serene Scandinavian bedroom with ${pick(furniture.BEDS)}, ${pick(textiles.BEDDING)} in layered neutrals, ${pick(flooring.WOOD_FLOORS)}, ${pick(architecture.WINDOWS)}, ${pick(furniture.SIDE_TABLES)} with ${pick(decor.CERAMICS)}, ${pick(textiles.CURTAINS)}, ${pick(palette.NEUTRALS)}, ${pick(lighting.MORNING)}, ${pick(mood.CALM_SERENE)}`
      break

    case 'kitchen':
      setting = `Modern Scandinavian kitchen with ${pick(flooring.SURFACES)}, floating ${pick(furniture.SHELVING)}, ${pick(architecture.WINDOWS)}, ${pick(decor.LIGHTING)} overhead, ${pick(decor.PLANTS)}, ${pick(palette.NEUTRALS)}, ${pick(lighting.NATURAL_LIGHT_QUALITY)}, ${pick(mood.TIMELESS)}`
      break

    case 'bathroom':
      setting = `Spa-like Scandinavian bathroom with ${pick(flooring.SURFACES)}, natural oak vanity, ${pick(architecture.WINDOWS)}, ${pick(textiles.THROWS)} on wooden stool, ${pick(decor.CERAMICS)}, ${pick(decor.PLANTS)}, ${pick(palette.NEUTRALS)}, ${pick(lighting.NATURAL_LIGHT_QUALITY)}, ${pick(mood.CALM_SERENE)}`
      break

    case 'dining':
      setting = `Scandinavian dining area with minimalist oak dining table, ${pick(furniture.CHAIRS)} arranged around, ${pick(decor.LIGHTING)} overhead, ${pick(architecture.WINDOWS)}, ${pick(decor.CERAMICS)} as centerpiece, ${pick(flooring.WOOD_FLOORS)}, ${pick(palette.OVERALL)}, ${pick(lighting.AFTERNOON)}, ${pick(mood.WARM_INVITING)}`
      break
  }

  return setting
}

/**
 * Detect interior room type from context
 */
export function detectRoomType(text: string): 'living' | 'bedroom' | 'kitchen' | 'bathroom' | 'dining' | null {
  const textLower = text.toLowerCase()

  if (/living room|lounge|sitting room|sofa|couch|living area/i.test(textLower)) return 'living'
  if (/bedroom|bed|sleeping|master bedroom|guest room/i.test(textLower)) return 'bedroom'
  if (/kitchen|cooking|dining|table|breakfast|meal/i.test(textLower)) {
    // Distinguish between kitchen and dining
    if (/dining table|dining area|dinner/i.test(textLower)) return 'dining'
    return 'kitchen'
  }
  if (/bathroom|bath|shower|spa|skincare/i.test(textLower)) return 'bathroom'

  return null
}

/**
 * Detect time of day from context
 */
export function detectTimeOfDay(text: string): 'morning' | 'afternoon' | 'evening' {
  const textLower = text.toLowerCase()

  if (/morning|breakfast|dawn|early/i.test(textLower)) return 'morning'
  if (/evening|night|dusk|sunset|dinner/i.test(textLower)) return 'evening'
  return 'afternoon' // Default
}
